        también el "switch". Estas estructuras permiten que el programa tome decisiones en función de si 
        se cumple o no una determinada condición.

        Estructuras de control de repetición: también conocidas como bucles o ciclos, permiten que el 
        programa repita una determinada acción un número de veces determinado o mientras se cumpla una
        determinada condición. Entre los bucles más comunes se encuentran el "for", el "while" y el 
        "do-while".
        
        Estructuras de control de excepciones: permiten manejar errores o situaciones inesperadas que 
        puedan ocurrir durante la ejecución del programa. Por ejemplo, las sentencias "try" y "catch" 
        en Java son estructuras de control de excepciones que permiten manejar errores en tiempo de 
        ejecución. 
    */
